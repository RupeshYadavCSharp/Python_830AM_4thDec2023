# code for withdrawal of money from bank
num = int(input("enter amount\n",))                             #enter amount = 1360
print ("500 *",num//500)                                        #500 * 2, num//500= 1360//500 =2
a = num - (num//500*500)                                        #a = 1360 - (1360//500*500)=360
print ("200 *",(a//200))                                        #200 * 1,a//200 = 360//200=1
b = a // 200                                                    #b=360//200=1
print ("100 *", ((num - ((num//500*500)+(b*200)))//100))        #100 * 1,((1360-((1360//500*500)+(1*200)))//100=1
c = (num-(((num//500*500)+(b*200))+((num-((num//500*500)+(b*200)))//100*100))) #c=1360-((1000+200)+((1360-(1000+200))//100*100))=60
d = (num -((num//500*500)+(b*200)+c))                           #d=(1360-((1360//500*500)+(1*200)+60))=100
print ("50 *", (num - ((num//500*500)+(b*200)+d))//50)          #50 * 1,(1360-(1000+200+100))//50=1
e = (num - ((num//500*500)+(b*200)+d))//50*50                   #e=(1360-(1000+200+100))//50*50
print ("10 *",(num-((num//500*500)+(b*200)+d+e))//10)           #10 * 1,(1360-(1000+200+100+50))//10=1